package Basic_Assignment;
//WAP=>Suppose the values of variables 'a' and 'b' are 6 and 8 respectively, write two programs to swap the values of the two variables.
//1 - first program by using a third variable
public class OperatorAssignment10_A {

	public static void main(String[] args) {
		int a=6,b=8,swap;
		swap=a;//swap=6 a=0
		a=b;//a=8 b=0
		b=swap;//b=6 swap=0
		System.out.println(a);
		System.out.println(b);


	}

}

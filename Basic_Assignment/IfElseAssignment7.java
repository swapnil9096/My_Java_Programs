/*Write a program to print absolute value of a number entered by user. E.g.-
INPUT: 1        OUTPUT: 1
INPUT: -1        OUTPUT: 1*/

public class IfElseAssignment7 {

	public static void main(String[] args) {
		int n=-134;
		if(n<=1)
		{
			System.out.println("1");
		}
		if(n>=1)
		{
			System.out.println("1");
		}
	}

}

package Basic_Assignment;
//WAP=>Print multiplication table of 24, 50 and 29 using loop.
public class LoopAssignment5 {

	public static void main(String[] args) {
		int n1=24,n2=50,n3=29;
		
for(int i=1;i<=10;i++)
{
	System.out.print(n1*i+"\t");
	System.out.print(n2*i+"\t");
	System.out.println(n3*i+"\t");
}
}

}

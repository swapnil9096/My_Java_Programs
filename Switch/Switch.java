package Switch;

public class Switch {

	public static void main(String[] args) {

		char vowels = 'w';

		switch (vowels) {
		case 'a':
			System.out.println("This is a vowel");
			break;

		case 'e':
			System.out.println("This is a vowel");
			break;

		case 'i':
			System.out.println("This is a vowel");
			break;

		case 'o':
			System.out.println("This is a vowel");
			break;

		case 'u':
			System.out.println("This is a vowel");
			break;
		case 'w' :
			System.out.println("This is real vowel");
			break;
		default:
			System.out.println("This is not vowel");
			break;

		}
	}

}

package BitwiseOperatorDemo;

public class BitwiseDemo {
	public static void main(String[] args) {
		
	
	System.out.println(Math.abs(-12));
	
	System.out.println(60&13); // 12
	System.out.println(60&16); //16
	System.out.println(60|13); // 61 
	System.out.println(60|16);  // 60
	System.out.println(60^13);  // 49
	System.out.println(60<<2);  // 240
	System.out.println(60>>2);  // 15
	}
}

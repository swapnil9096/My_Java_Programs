package While_Loop_Programs;

public class Table1T10 {

	public static void main(String[] args) {
   int i=1,j=1;
  
   while(i<=10)
   {
	    j=1;//2 
	   	   while(j<=10)
	   {
		   
	   	  System.out.print(i*j+"\t ");
	   	
	   	  j++;	
	   	
	   }
	   i++;
		System.out.println();
	   }
   			

	}

}
